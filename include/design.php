            <p id="desc">I expect most people viewing this site will be on PCs, but I wanted to demonstrate the importance of mobile first development. In the first iteration of this portfolio it had seperate pages you could navigate to via the navBar. I decided for mobile devices to switch it to a single page they could scroll and swipe to view as much as possible without constantly opening the nav button. There is still a navBar however for mobile devices it stays at the top of the site rather than fixed on the screen. I did add a fixed button on the bottom to return to top for easy direct navigation if the user would like. <br>Baguettebox is a great way to make a gallery small that users can quickly open and view all the pictures without opening a new page, and owl-caresoul allows you to have cards not taking up large amounts of screen space that users can easily swipe through. With one exception the design was about ease of navigation and reduced use of screen space, the "About Me" section I needed for a cool animation effect using scrollreveal.</p>
